# Example 1: A simple string
example_1 = "Hello, World!"

# Example 2: A multiline string
example_2 = """This is
a multiline
string."""

# Example 3: A string with special characters
example_3 = "Special characters: \n (newline), \t (tab), \\ (backslash)"

# Example 4: A raw string
example_4 = r"This is a raw string: \n (not a newline), \\ (backslash)"

# Example 5: String concatenation
example_5 = "Hello, " + "World!"

# Example 6: A string using single quotes
example_6 = 'This is a string in single quotes.'

print(example_1, example_2, example_3, example_4, example_5, example_6)



